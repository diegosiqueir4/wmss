package de.wwu.wmss.core;

import java.util.Date;

public class Interval {

	private Date from;
	private Date to;
	
	public Interval() {
		super();
	}
	
	public Date getFrom() {
		return from;
	}
	public void setFrom(Date from) {
		this.from = from;
	}
	public Date getTo() {
		return to;
	}
	public void setTo(Date to) {
		this.to = to;
	}
	
	
}
