package de.wwu.wmss.core;

public class Person {

	private String name;
	private String role;

	public Person() {
		super();

	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	
	
}
