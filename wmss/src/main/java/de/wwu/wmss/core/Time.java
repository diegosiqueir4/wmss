package de.wwu.wmss.core;

public class Time {

	private String time = "";
	private String format = "";
	
	public Time() {
	
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}
	
	
}
